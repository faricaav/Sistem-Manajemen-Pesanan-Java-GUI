    private void setTableListener() {
        jTable1.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent event) {
                if (!event.getValueIsAdjusting()) {
                    int selectedRow = jTable1.getSelectedRow();
                    if (selectedRow != -1) { 
                        String idPesanan = jTable1.getValueAt(selectedRow, 0).toString();
                        String pelanggan = jTable1.getValueAt(selectedRow, 1).toString();
                        String produk = jTable1.getValueAt(selectedRow, 2).toString();
                        String jumlah = jTable1.getValueAt(selectedRow, 3).toString();
                        String total = jTable1.getValueAt(selectedRow, 4).toString();
                        String status = jTable1.getValueAt(selectedRow, 5).toString();

                        valueID.setText(idPesanan);
                        valuePelanggan.setText(pelanggan);
                        valueProduk.setText(produk);
                        valueJumlah.setText(jumlah);
                        valueTotal.setText(total);
                        selectStatus.setSelectedItem(status);
                    }
                }
            }
        });
    }
    
