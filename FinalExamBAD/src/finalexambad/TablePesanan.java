    private void searchData(String keyword) {
        String query = "SELECT pesanan.id_pesanan, pelanggan.nama_pelanggan, produk.nama_produk, pesanan.jumlah, pesanan.total, pesanan.status "
                     + "FROM pesanan "
                     + "JOIN pelanggan ON pesanan.id_pelanggan = pelanggan.id_pelanggan "
                     + "JOIN produk ON pesanan.id_produk = produk.id_produk "
                     + "WHERE pelanggan.nama_pelanggan LIKE ? OR produk.nama_produk LIKE ? OR pesanan.id_pesanan = ? OR pesanan.status LIKE ?";
        try {
            PreparedStatement ps = con.prepareStatement(query);
            ps.setString(1, "%" + keyword + "%");
            ps.setString(2, "%" + keyword + "%");
            ps.setString(3, keyword);
            ps.setString(4, "%" + keyword + "%");
            ResultSet rs = ps.executeQuery();

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0); // Mengosongkan tabel sebelum menampilkan hasil pencarian baru

            while (rs.next()) {
                Object[] row = {
                    rs.getString("id_pesanan"),
                    rs.getString("nama_pelanggan"),
                    rs.getString("nama_produk"),
                    rs.getString("jumlah"),
                    rs.getString("total"),
                    rs.getString("status")
                };
                model.addRow(row);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error dalam melakukan pencarian: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void getData() {
        String query = "SELECT pesanan.id_pesanan, pelanggan.nama_pelanggan, produk.nama_produk, pesanan.jumlah, pesanan.total, pesanan.status "
                     + "FROM pesanan "
                     + "JOIN pelanggan ON pesanan.id_pelanggan = pelanggan.id_pelanggan "
                     + "JOIN produk ON pesanan.id_produk = produk.id_produk";
        try {
            PreparedStatement ps = con.prepareStatement(query);
            ResultSet rs = ps.executeQuery();

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0); // Mengosongkan tabel sebelum menampilkan hasil pencarian baru

            while (rs.next()) {
                Object[] row = {
                    rs.getString("id_pesanan"),
                    rs.getString("nama_pelanggan"),
                    rs.getString("nama_produk"),
                    rs.getString("jumlah"),
                    rs.getString("total"),
                    rs.getString("status")
                };
                model.addRow(row);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error dalam menampilkan data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
 
