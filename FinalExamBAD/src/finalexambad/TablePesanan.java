    private boolean updateStatus(int idPesanan, String newStatus) {
        String query = "UPDATE pesanan SET status = ? WHERE id_pesanan = ?";
        try {
            PreparedStatement ps = con.prepareStatement(query);
            ps.setString(1, newStatus);
            ps.setInt(2, idPesanan);
            int updatedRows = ps.executeUpdate();
            if (updatedRows > 0) {
                JOptionPane.showMessageDialog(this, "Status berhasil diperbarui.", "Success", JOptionPane.INFORMATION_MESSAGE);
                return true;
            } else {
                JOptionPane.showMessageDialog(this, "Gagal memperbarui status.", "Error", JOptionPane.ERROR_MESSAGE);
                return false;
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error dalam memperbarui status: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }

